const generatorBtn = document.querySelector("#generator-btn");
let output = document.querySelector("#pw");
const clipboardIcon = document.querySelector(".fa-clipboard");
const tooltipText = document.querySelector(".tooltiptext");

generatorBtn.addEventListener("click", () => {
	output.textContent = "";
	for (let i = 0; i < 16; i++) {
		let chars = Math.floor(Math.random() * characters.length);
		output.textContent += characters[chars];
	}
	tooltipText.textContent = "Copy to clipboard";
});

clipboardIcon.addEventListener("click", () => {
	navigator.clipboard.writeText(output.textContent);
	tooltipText.textContent = "Copied!";
});
